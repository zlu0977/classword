package introduction;

public class ProceduralExample {

	public static void main(String[] args) {
		String[] names = {"Bob", "Jillian", "Obamaa"};
		String[] years = {"Freshman", "Staff", "Junior", "Senior"};
		System.out.println("Hi, my name is " + names[0] + ". I am a " + years[0]);
		System.out.println("Hi, my name is " + names[1] + ". I am a " + years[1]);
		System.out.println("Hi, my name is " + names[2] + ". I am a " + years[2]);
	}

}
